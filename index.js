function randomEl(array) {
  return array[Math.floor(Math.random() * array.length)];
}

function replaceCharacter(char) {
  var charGroup = mapping[char];
  if (!charGroup) {
    return char;
  }
  var newChar = randomEl(charGroup);
  if (Array.isArray(newChar)) {
    newChar = randomEl(newChar);
  }
  return newChar;
}

function transform(word) {
  return word
    .split("")
    .map(replaceCharacter)
    .join("");
}

